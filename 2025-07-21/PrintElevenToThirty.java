public class PrintElevenToThirty {
    static void printElevenToThirty() {
        for (int i = 11; i <= 30; i++) {
            if (i % 2 == 0) {
                System.out.println(i);
            }

        }
    }

    public static void main(String[] args) {
        printElevenToThirty();
    }
}
 